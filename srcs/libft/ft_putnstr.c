#include "libft.h"

int		ft_putnstr(const char *str, long int end)
{
	int	i;

	i = 0;
	if (str)
	{
		while (str[i] != '\0' && i < end)
		{
			write(1, str + i, 1);
			i++;
		}
	}
	return (i);
}
